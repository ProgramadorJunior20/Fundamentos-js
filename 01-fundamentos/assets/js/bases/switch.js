

const dia = 4; // 0: Domingo 1: Lunes ......

switch ( dia ) {
    case 0:
        console.log('Domingo');
        break;
    case 1:
        console.log('Lunes');
        break;
    case 2:
        console.log('Martes');
        break;
    case 3:
        console.log('Miercoles');
        break;
    default:
        console.log('No es Lunes, Martes, Miercoles o Domingo');
}